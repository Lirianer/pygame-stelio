class Circle():

	def __init__(self, x, y, radius):
		self.x = x
		self.y = y
		self.radius = radius

	def setX(self, x):
		self.x = x

	def setY(self, y):
		self.y = y

	def setRadius(self, radius):
		self.radius = radius

	def getX(self):
		return self.x

	def getY(self):
		return self.y

	def getRadius(self):
		return self.radius